	<td id="colone"><h2>CounterMail - the secure email provider</h2>
<img id="imgright" vspace=10  src="images/gridlock.gif">
<!-- <img id="imgright" hspace=10 src="images/istockglockgridlock3.gif"> -->
<p>CounterMail is a secure and easy to use online email service, designed to provide maximum security and privacy without any unnecessary complexity.</p>
<p>You can access your email account at any time, from anywhere in the world. Your account will always be encrypted and anonymous.</p>
<p>CounterMail's encryption works automatically and transparently, it requires no specialized computer skills or knowledge. If you are comfortable using services like Hotmail or Gmail, you will be comfortable using CounterMail's secure email!</p>
<p>We are using a strong encryption protocol called OpenPGP, with 4096 bits encryption <a target='_blank' href="http://en.wikipedia.org/wiki/Public-key_cryptography">keys</a> to protect your data. To the best of publicly available information, there is no known method which will allow a person or group to break OpenPGP:s encryption by cryptographic or computational means.</p>
